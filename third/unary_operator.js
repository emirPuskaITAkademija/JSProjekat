/**
 * Unarni operatori:
 *   ++
 *    1.1 post inkrement  number1++
 *    1.2 pre inkrement   ++number1
 *   --
 */
let number1 = 23;
console.log(number1++);//  poslati u konzolu 23  i uvecati na 24
console.log(++number1);// 25
console.log(number1);//25

number1 = 10;
console.log(number1--); // 10 -> 9
console.log(--number1); // 8
console.log(number1); // 8